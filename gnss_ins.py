class EKF_GNSS_INS:
    def __init__(self, dt, std_acc, std_yaw_rate, std_gps):
        self.dt = dt
        self.x = np.zeros((4, 1)) # [x, y, v, theta]               #Robotun durumunu tutar (Konum X, Konum Y, Hız, Yön). Başlangıçta hepsi 0.
        self.P = np.eye(4)                                         #Güven matrisi. Köşegen birim matristir. Başlangıçta "neredeyim pek emin değilim" der.
        self.Q = np.diag([0, 0, std_acc**2, std_yaw_rate**2])      #Sistem Gürültüsü. İvmeölçer ve jiroskopa ne kadar güvenmediğimizi tanımlarız.
