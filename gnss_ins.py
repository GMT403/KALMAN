# --- 1. EKF SINIFI (GNSS/INS ENTEGRASYONU) ---
class EKF_GNSS_INS:
    def __init__(self, dt, std_acc, std_yaw_rate, std_gps):
        self.dt = dt
        self.x = np.zeros((4, 1)) # [x, y, v, theta]
        self.P = np.eye(4)        # Kovaryans
        self.Q = np.diag([0, 0, std_acc**2, std_yaw_rate**2]) # İşlem Gürültüsü
        self.R = np.diag([std_gps**2, std_gps**2])            # Ölçüm Gürültüsü
        self.H = np.array([[1, 0, 0, 0], [0, 1, 0, 0]])       # Ölçüm Matrisi
        self.I = np.eye(4)

    def predict(self, u):
        theta = self.x[3, 0]
        v = self.x[2, 0]
        a = u[0]
        omega = u[1]
        dt = self.dt

        # Hareket Modeli
        new_x = self.x[0, 0] + v * np.cos(theta) * dt
        new_y = self.x[1, 0] + v * np.sin(theta) * dt
        new_v = v + a * dt
        new_theta = theta + omega * dt
        self.x = np.array([[new_x], [new_y], [new_v], [new_theta]])

        # Jakobiyen (F)
        F = np.eye(4)
        F[0, 3] = -v * np.sin(theta) * dt
        F[0, 2] = np.cos(theta) * dt
        F[1, 3] = v * np.cos(theta) * dt
        F[1, 2] = np.sin(theta) * dt

        # Q Gürültüsünü Duruma Yayma
        Q_noise = np.diag([0.1 * dt**2, 0.1 * dt**2, self.Q[2, 2] * dt**2, self.Q[3, 3] * dt**2])
        self.P = F @ self.P @ F.T + Q_noise

    def update(self, z):
        z_pred = self.H @ self.x
        y = z - z_pred
        S = self.H @ self.P @ self.H.T + self.R
        K = self.P @ self.H.T @ np.linalg.inv(S)
        self.x = self.x + K @ y
        self.P = (self.I - K @ self.H) @ self.P

